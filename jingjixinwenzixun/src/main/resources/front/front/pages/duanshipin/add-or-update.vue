					duanshipinValue: '',//数字对应的值
					duanshipinContent: '',
					duanshipinDelete: '',
					insertTime: '',
					createTime: '',
            },
            // 登陆用户信息
            user: {},
				duanshipinTypesOptions: [],
							duanshipinTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {

		/*下拉框*/
			let duanshipinTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'duanshipin_types',
            }
			let duanshipinTypes = await this.$api.page(`dictionary`, duanshipinTypesParams);
			this.duanshipinTypesOptions = duanshipinTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`duanshipin`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.duanshipinId){
                this.ruleForm.duanshipinId = options.duanshipinId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            duanshipinTypesChange(e) {
                this.duanshipinTypesIndex = e.target.value
                this.ruleForm.duanshipinValue = this.duanshipinTypesOptions[this.duanshipinTypesIndex].indexName
                this.ruleForm.duanshipinTypes = this.duanshipinTypesOptions[this.duanshipinTypesIndex].codeIndex
            },


            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },


			duanshipinPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.duanshipinPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			duanshipinVideoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.duanshipinVideo = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },




            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.duanshipinName)) {
                    this.$utils.msg(`短视频名称不能为空`);
                    return
                }
				if ((!this.ruleForm.duanshipinPhoto)) {
                    this.$utils.msg(`短视频照片不能为空`);
                    return
                }
				if ((!this.ruleForm.zanNumber) && (!this.$validate.isIntNumer(this.ruleForm.zanNumber)) && this.ruleForm.zanNumber >0) {
                    this.$utils.msg(`赞不能为空，不能小于0 格式为数字`);
                    return
                }
				if ((!this.ruleForm.caiNumber) && (!this.$validate.isIntNumer(this.ruleForm.caiNumber)) && this.ruleForm.caiNumber >0) {
                    this.$utils.msg(`踩不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`duanshipin`, this.ruleForm);
                } else {
                    await this.$api.save(`duanshipin`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
